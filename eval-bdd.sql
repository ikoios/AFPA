CREATE DATABASE IF NOT EXISTS Magasin;

USE Magasin;

CREATE TABLE IF NOT EXISTS Agence(noAg INT AUTO_INCREMENT PRIMARY KEY,
                                    nomAg VARCHAR(25));

CREATE TABLE IF NOT EXISTS Client(coCig INT AUTO_INCREMENT PRIMARY KEY,
                                    nomCli VARCHAR(25),
                                    rueCli VARCHAR(25),
                                    villeCli VARCHAR(25),
                                    noAg INT);

CREATE TABLE IF NOT EXISTS Fournisseur(noFrs INT AUTO_INCREMENT PRIMARY KEY,
                                        nomFrs VARCHAR(25),
                                        adrFrs VARCHAR(25),
                                        tel INT);

CREATE TABLE IF NOT EXISTS Commande(noBC INT AUTO_INCREMENT PRIMARY KEY,
                                    dateBC DATE,
                                    coCli INT,
                                    noFrs INT);

CREATE TABLE IF NOT EXISTS Facture(noFac INT AUTO_INCREMENT PRIMARY KEY,
                                    dateFac DATE);

CREATE TABLE IF NOT EXISTS Produit(refProd INT AUTO_INCREMENT PRIMARY KEY,
                                    desiProd VARCHAR(25),
                                    codeTva INT);

CREATE TABLE if NOT EXISTS Concerner(noBC INT NOT NULL,
                                        refProd INT NOT NULL,
                                        qteCom INT,
                                        pUnit VARCHAR(25),
                                        FOREIGN KEY (noBC) REFERENCES Commande(noBC),
                                        FOREIGN KEY (refProd) REFERENCES Produit(refProd));

CREATE TABLE IF NOT EXISTS Livraison(noBL INT AUTO_INCREMENT PRIMARY KEY,
                                        dateBL DATE,
                                        noBc INT,
                                        noFac INT);

CREATE TABLE IF NOT EXISTS Composer(noBL INT NOT NULL,
                                    refProd INT NOT NULL,
                                    qteLiv INT,
                                    FOREIGN KEY (noBL) REFERENCES Livraison(noBL),
                                    FOREIGN KEY (refProd) REFERENCES Produit(refProd));

CREATE TABLE if NOT EXISTS TVA(codeTva INT AUTO_INCREMENT PRIMARY KEY,
                                tauxTva INT);
